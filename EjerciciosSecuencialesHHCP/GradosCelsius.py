"""
Este programa convierte los grados Fahrenheit a grados Celsius
Solicitamos Datos
Grados Fahrenheit
Formula para hacer calculos
C =  (F-32) x 5/9
Don C son los grados Celcius y la F son los Fahrenheit

"""

Fahrenheit = float(input("Introduce los grados fahrenheit: "))
# C =  (F-32) x 5/9
print("Los grados Celsius son", (Fahrenheit - 32) * 5 / 9,"ºC")

